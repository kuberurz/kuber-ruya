export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' });

  const { prompt, image, imageType } = req.body;
  if (!prompt) return res.status(400).json({ error: 'Prompt gerekli' });

  // Fotoğraf varsa görsel içerikli mesaj oluştur
  let messageContent;
  if (image) {
    messageContent = [
      {
        type: 'image',
        source: { type: 'base64', media_type: imageType || 'image/jpeg', data: image }
      },
      { type: 'text', text: prompt }
    ];
  } else {
    messageContent = prompt;
  }

  try {
    const response = await fetch('https://api.anthropic.com/v1/messages', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'x-api-key': process.env.ANTHROPIC_API_KEY,
        'anthropic-version': '2023-06-01'
      },
      body: JSON.stringify({
        model: 'claude-haiku-4-5-20251001',
        max_tokens: 1200,
        messages: [{ role: 'user', content: messageContent }]
      })
    });

    const data = await response.json();
    if (!response.ok) throw new Error(data.error?.message || 'API hatası');

    const text = data.content?.[0]?.text || '';
    res.status(200).json({ result: text });
  } catch (err) {
    res.status(500).json({ error: err.message });
  }
}
